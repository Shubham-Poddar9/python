def add(a,b):
    return a+b
def subt(a,b):
    return a-b
def mult(a,b):
    return a*b
def div(a,b):
    return a/b
def floord(a,b):
    return a//b
def mod(a,b):
    return a%b
def expo(a,b):
    return a**b
